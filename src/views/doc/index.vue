      <article class="article">
        <header class="article-hero">
          <h2>一条研发路线是如何被计算出来的？</h2>
          <p>
            当你按下一键计算时，算法并不是简单地从目标载具向上绘制一条线。它所要考虑的情况和需要处理的问题，远远比我们想象中更加复杂。算法在指向箭头所包含的前置依赖关系、等级开放数量、已拥有载具执行策略、高级载具执行策略与特殊参数设置同时存在的情况下，找出一条能够实际执行的完整路线。
          </p>
          <p>
            我们把整个过程想象成一次远征：目标载具是终点，科技树中的连线是道路，各等级的开放数量是沿途关卡，而你已经拥有的载具与预备的高级载具，则是出发前已经掌握的资源。规划算法所做的，就是在遵守所有规则的前提下，为这次远征安排一条最合适的路线。
          </p>
        </header>

        <section>
          <h3>计算开始前，你的每一种选择都有不同的含义</h3>
          <p>
            在算法开始运作前，你会先在科技树上执行一些交互操作。尽管这看起来都是在“选中某个载具”，但它们在算法中的身份并不相同。普通可研发的银狮载具如果已经被你选中，会被视为“已拥有载具”。算法认可它对等级数量的贡献，也可以从它们开始继续规划剩余路线，却不会再把它们以及它们之前的研究费用算进总和。它们就像一座已经抵达的补给站，路线可以从这里开始继续前进。
          </p>
          <p>
            被选中的高级载具则被视为预备载具，它们也是“已拥有载具”的一部分，它们不需要研发，也不会增加研发点数总和，但只要所在等级低于最高目标，它们就可以参与该等级的开放数量统计。目标载具的意义最直接：它们是本次规划必须抵达的终点。多个目标可以同时存在，算法会把这些目标所要求的道路合并起来，共用的部分只计算一次。
          </p>
          <p>
            在算法开始时，一级载具会执行特殊的规则：研发点数为零的初始载具，无论它是否藏在折叠组里，都会在规划开始时自动视为已经选中的“已拥有载具”。这些载具本身就是路线的起点，不会因为你勾选了“绕开折叠载具”而消失。
          </p>
        </section>

        <section>
          <h3>计算开始时，科技树会被还原成一张真正可行走的路线图</h3>
          <p>
            科技树的视觉排列（也就是你肉眼所看到的样子）并不等同于研究关系。算法会按照载具所在国家、军种、等级、列与折叠组位置，重新认识每辆载具的前置依赖关系。正常向下延伸的箭头意味着后续载具必须建立在前一辆载具之后；被标记为路线终点的载具则会截断这条道路，避免算法错误地把同一列中视觉位置更低、实际上并不相连的载具衔接在后面。
          </p>
          <p>
            折叠载具会被当作一个有次序的组。组内第一辆是这组的入口，若明确选择组内更靠后的载具，它之前的组员也会成为必要条件（等同于前置依赖关系）。与此同时，科技树主线继续向下延伸时，通常以折叠组的第一辆载具作为连接点。这种处理既保留了游戏中的正确折叠关系，也避免把整个折叠组无条件塞进路线中。
          </p>
          <p>
            除了上下载具的前置依赖关系，在算法中还有另一个很重要的机制：“前置路线”。所谓的“前置路线”，并不只是一条直线。一个目标载具会带来自己的一系列必经载具，而另一个目标载具又会带来另外一系列的必经载具，这就是“支线”。算法会先收集全部不可回避的部分，再考虑用哪些额外载具满足等级门槛。
          </p>
        </section>

        <section>
          <h3>对算法而言，真正让路线变复杂的，是每个等级的开放数量</h3>
          <p>
            抵达目标并不只是研究它的前置载具那么简单，在原游戏中要求玩家在一个等级拥有足够数量的载具之后才能开放下一个等级。因此算法也必须严格遵循这一点。但在算法的初期版本中（researchPathPlanner_beta
            1.0.3<）这样的条件给算法带来了灭顶之灾，在科技树中搜索次数达到12000次以后，算法会强行结束并给出最终结果，很显然结果并非全都在预期之中。但经过多次的内部版本迭代，目前的算法已足够智能，至少不会再出现这个问题。目前算法会找到所有目标中等级最高的那一个，并检查它之前的每个等级都必须达到当前国家与军种规定的最低数量。
          </p>
          <p>
            这里的数量是最低要求，而不是必须刚好相等。也就是说，如果目标的必经路线本身已经让某一级超过门槛，算法不会为了凑成一个漂亮的数字而删除必要载具。已拥有的普通银狮载具和预备高级载具以及本次路线规划过程中由算法选中的普通银狮载具，都能对相应等级的数量作出贡献。算法最终寻找的，是一份“所有门槛都已达到”的完整方案，而不是只看目标附近的一小段道路。
          </p>
        </section>

        <section>
          <h3>
            路径求解器：Soft 模式（先求最短路径，再基于条件考虑你的偏好列）
          </h3>
          <p>
            当你没有勾选“强制执行列偏好”选项时，算法执行的是 Soft
            模式，该模式承担的是精确最短路线的职责。这里的“最短”有一个明确含义：在所有推演出来的合法方案中，总研发点数必须是最少的。算法不会因为看到一辆便宜的载具就立刻选择它，眼前的便宜并不代表整条路线便宜；一辆载具可能带来更长的前置路线，也可能让后面某个等级更容易开放，算法必须考虑到这些连锁反应。
          </p>
          <p>
            为了做到这一点，算法会沿着每一条独立的路线考察“选择这辆”与“暂不选择这辆”可能形成的结果，并为每一个等级的完成进度建立一份对照账本。若两种选择带来的等级进度完全相同，账本只保留研发点数更少的那一种；如果研发点数完全相同，则保留更符合列偏好的那一种。这也就解释了当你在没有勾选“强制执行列偏好”时为什么最终结果通常不会沿着你所选择的列偏好发展的原因。那些已经被证明不可能优于现有记录的路线会全部被算法废弃，而仍有机会成为最佳答案的路线会继续参与组合。
          </p>
          <p>
            这种方式不是凭经验猜测，也不是在若干条看起来不错的路线中随便挑选。算法实际上会把仍可能成为答案的情况进行完整比较，直到得到能够证明的最少研发点方案。因此，Soft
            模式返回成功时，结果不仅是一条可用路线，更是在当前输入规则下真正的最低研发点路线。
          </p>
          <p>
            列偏好选项在 Soft
            模式中是第二判断标准。只有当两份方案的总研发点数完全相同时，算法才会选择包含更多偏好列载具的方案。Soft
            模式不会为了靠近偏好列而多付出哪怕1点研发点。若选择了多个偏好列，它们在
            Soft
            模式中会被视为一个偏好范围，算法关心的是最终方案对这些列的总体命中程度，而不是直接执行它们。
          </p>
        </section>

        <section>
          <h3>路径求解器：Hard 模式（在你的偏好列中求最短路径）</h3>
          <p>
            当你勾选“强制执行列偏好”选项时（默认勾选），算法执行的是 Hard
            模式。该模式适合已经明确希望沿着某些列发展的用户。它会按照用户指定的列偏好顺序，优先尝试能够从这些列补足等级数量的方案；在偏好条件相近时，才参考新增的研发点数、载具位置和折叠关系等因素。找到一份满足目标、前置依赖关系和所有等级门槛的方案后，就可以结束本次搜索了。
          </p>
          <p>
            这意味着 Hard 模式的承诺与 Soft
            模式大有不同。它承诺的是“优先照顾路线方向”，而不是“全局最低研发点数”。结果往往比全局最短路线昂贵，但更接近用户打算长期发展的列。当科技树数据规模很大、目标节点很多或偏好条件十分复杂时，Hard
            模式还设有安全搜索上限，避免一次性大规模运算长时间占用CPU性能；如果触及上限，系统会明确提示结果是否完成（在偏好设置中启用开发者选项时可用），而不会把未完成的搜索伪装成最短路线。
          </p>
        </section>

        <section>
          <h3>主线、列偏好与折叠载具之间如何影响结果</h3>
          <p>
            目标载具所在列会被标记为“主线”。主线已经是抵达目标不可回避的方向，因此不会再次出现在可选列偏好中，也不会被重复计算成额外倾向。若某一列先前已经被选为偏好，后来又有该列载具被设为目标，这一列会自动从偏好列表中移除以避免出现不必要的错误。
          </p>
          <p>
            当你勾选“绕开折叠载具”后，每个折叠组只有第一辆载具可以作为补足等级数量的普通候选，同组后续折叠载具不会为了凑数而被加入。不过，这不是对折叠载具的绝对封锁：如果某辆后续折叠载具本身就是目标，或是抵达目标不可缺少的条件，它仍会被正常选中。等级一的研发点数为零的折叠载具仍然全部选中，因为它们属于初始载具。
          </p>
        </section>

        <section>
          <h3>RP 决定路线如何推演，SP 只是一个副作用属性</h3>
          <p>
            RP 是研发点数的缩写简称，SP 是银狮的缩写简称。算法不会基于 SP
            决定走哪条路线。无论是 Soft 模式还是 Hard 模式，SP
            都不会参与到候选路线之间的高低比较。尤其在 Soft 模式中，如果两条路线
            RP 相同，其中一条更符合列偏好，即使它需要更多
            SP，算法仍会选择偏好路线。
          </p>
          <p>
            这并不意味着 SP 被忽略。路线确定后，算法会逐辆汇总新研究载具的 RP 与
            SP，页面也会继续计算各等级和整份方案的资源总量。区别只在于：RP
            是规划方向的依据，SP
            是结果形成后的载具购买所需货币。已经拥有的载具可以贡献等级数量，但不会重复计入最终的
            RP / SP 总和；高级载具同样不增加普通研究路线的 RP 与 SP。
          </p>
        </section>

        <section>
          <h3>从按下一键计算到看到结果的完整过程发生了什么</h3>
          <p>
            一次规划开始后，算法先确认目标是否有效，并辨认用户已拥有的普通银狮载具与预备高级载具。随后，它收集所有目标的必要前置路线，把已拥有载具当作新的起点，并加入初始载具。接下来，在计算最终目标之前每一级还缺多少数量，再依据
            Soft 或 Hard 模式搜索合适的补位载具。
          </p>
          <p>
            在搜索过程中，任何候选载具都不能破坏前置依赖关系，也不能绕过等级门槛；折叠限制、终点位置和列偏好会在各自应该发挥作用的阶段介入。方案确定后，算法会重新统计各等级的载具数量，汇总
            RP 与
            SP，并把结果映射回科技树。原先选中的普通银狮载具会显示为已拥有，目标与新增路线则共同构成最终成果。如果用户希望撤销，还可以回退到自动计算前的手动选择状态。
          </p>
        </section>

        <section>
          <h3>计算结果的保证说明及其生效条件与边界范围</h3>
          <p>
            Soft 模式的“最短路径保证”限定为总 RP
            最低，并且必须遵循用户提供的已拥有载具、预备高级载具、折叠设置和目标载具。它不是载具数量最少，也不是
            SP 最少。Hard 模式则不提供最低 RP
            保证，因为它主动把列方向放到了更重要的位置。理解这一区别，才能正确判断结果为什么这样选择，也能知道应该在什么时候切换模式。
          </p>
          <p>
            一键计算并不是替玩家决定要玩什么。它更像一名严谨的参谋：先替你守住游戏规则，再计算资源代价，最后在允许的范围内尊重你的发展方向。目标由你决定，偏好由你决定，而算法负责让这份计划完整、可执行，并且清楚地说明它究竟优先了什么。
          </p>
        </section>

        <section>
          <h3>算法难免会犯错，但极其微小的错误可以忽略</h3>
          <p>
            尽管在上线前已经对算法进行了多阶段、大规模、全阵营覆盖的矩阵测试以及两次真人测试组实际交互测试，但目前已知的问题中仍然存在一个影响不大的设计缺陷：在部分规划中，算法可能在一级载具里误选了 RP 点数更高的载具，但考虑到一级载具的研发点数较低，通常这个误差不会超过3000研发点。正常情况下一条路线的总和都在300万以上，误差远不足 0.1%，可忽略不计，后续不会考虑修复此问题。
          </p>
        </section>
